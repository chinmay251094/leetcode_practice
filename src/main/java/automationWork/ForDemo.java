package automationWork;

public class ForDemo {
    public static void main(String[] args) {
        for (int i = 0; i < 1000; i++) {
            System.out.println(i+ " My name is bhargey");
        }

        int i = 1;
        while(i < 101) {
            System.out.println(i+ " My name is bhargey");
            i++;
        }
    }
}
